void questao3() {
  // Sequência a)
  int a1 = 1;
  int a2 = 3;
  int a3 = 5;
  int a4 = 7;
  int a5 = a4 + 2; // Próximo elemento é o último elemento + 2
  print("Próximo elemento da sequência a): $a5");

  // Sequência b)
  int b1 = 2;
  int b2 = 4;
  int b3 = 8;
  int b4 = 16;
  int b5 = 32;
  int b6 = 64;
  int b7 = b6 * 2; // Próximo elemento é o último elemento * 2
  print("Próximo elemento da sequência b): $b7");

  // Sequência c)
  int c1 = 0;
  int c2 = 1;
  int c3 = 4;
  int c4 = 9;
  int c5 = 16;
  int c6 = 25;
  int c7 = c6 + (c6 - c5); // Próximo elemento é o último elemento + a diferença entre os dois últimos elementos
  print("Próximo elemento da sequência c): $c7");

  // Sequência d)
  int d1 = 4;
  int d2 = 16;
  int d3 = 36;
  int d4 = 64;
  int d5 = 100;
  int d6 = d5 + (d5 - d4); // Próximo elemento é o último elemento + a diferença entre os dois últimos elementos
  print("Próximo elemento da sequência d): $d6");

  // Sequência e)
  int e1 = 1;
  int e2 = 1;
  int e3 = 2;
  int e4 = 3;
  int e5 = 5;
  int e6 = 8;
  int e7 = e6 + e5; // Próximo elemento é a soma dos dois últimos elementos
  print("Próximo elemento da sequência e): $e7");

  // Sequência f)
  int f1 = 2;
  int f2 = 10;
  int f3 = 12;
  int f4 = 16;
  int f5 = 17;
  int f6 = 18;
  int f7 = 19;
  int f8 = f7 + 1; // Próximo elemento é o último elemento + 1
  print("Próximo elemento da sequência f): $f8");
}
